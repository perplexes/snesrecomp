//! `sync-funcs-h` — Rust port of `tools/v2_sync_funcs_h.py`.
//!
//! Emits the v2-shape `recomp/funcs.h`: every function declared in any bank cfg
//! (`func` line or `name` decl) becomes five forward declarations — a bare
//! `void <name>(CpuState *cpu);` alias plus the four `(m, x)` variants.

use std::collections::HashSet;
use std::path::PathBuf;

use snesrecomp_regen::cfg::load_bank_cfg;

fn arg_value(args: &[String], flag: &str) -> Option<String> {
    args.iter().position(|a| a == flag).and_then(|i| args.get(i + 1).cloned())
}

/// Extract the bank from a `bankXX.cfg` filename (matches `_BANK_CFG_RE`:
/// `bank([0-9a-fA-F]+)\.cfg$`).
fn filename_bank(p: &std::path::Path) -> Option<u32> {
    let name = p.file_name()?.to_str()?;
    let rest = name.strip_prefix("bank")?;
    let hex = rest.strip_suffix(".cfg")?;
    if hex.is_empty() || !hex.chars().all(|c| c.is_ascii_hexdigit()) {
        return None;
    }
    u32::from_str_radix(hex, 16).ok()
}

/// Return (name, addr) pairs, deduped by name (first-seen address wins),
/// sorted by address (stable on ties → first-seen order).
fn collect_function_names(cfg_dir: &std::path::Path) -> Vec<(String, u32)> {
    let mut cfg_paths: Vec<PathBuf> = std::fs::read_dir(cfg_dir)
        .expect("read cfg dir")
        .filter_map(|e| e.ok().map(|e| e.path()))
        .filter(|p| {
            p.file_name()
                .and_then(|n| n.to_str())
                .map(|n| n.starts_with("bank") && n.ends_with(".cfg"))
                .unwrap_or(false)
        })
        .collect();
    cfg_paths.sort();

    let mut seen_names: HashSet<String> = HashSet::new();
    // insertion-ordered (name, addr) — stable sort by addr keeps first-seen on ties.
    let mut order: Vec<(String, u32)> = Vec::new();

    for p in &cfg_paths {
        let bank = match filename_bank(p) {
            Some(b) => b,
            None => continue,
        };
        let cfg = match load_bank_cfg(p) {
            Ok(c) => c,
            Err(e) => {
                eprintln!("  parse-fail {}: {e}", p.display());
                continue;
            }
        };
        for entry in &cfg.entries {
            if let Some(name) = &entry.name {
                if !name.is_empty() && !seen_names.contains(name) {
                    seen_names.insert(name.clone());
                    order.push((name.clone(), (bank << 16) | (entry.start & 0xFFFF)));
                }
            }
        }
        for nd in &cfg.names {
            if !nd.name.is_empty() && !seen_names.contains(&nd.name) {
                seen_names.insert(nd.name.clone());
                order.push((nd.name.clone(), nd.addr_24 & 0xFFFFFF));
            }
        }
    }

    // Stable sort by address (Python `sorted(..., key=addr)` is stable).
    order.sort_by_key(|kv| kv.1);
    order
}

fn emit_funcs_h(items: &[(String, u32)]) -> String {
    let mut lines: Vec<String> = Vec::new();
    lines.push("/* Auto-generated by snesrecomp v2 tools/v2_sync_funcs_h.py.".to_string());
    lines.push(" * Do NOT hand-edit. Every recompiled function takes a".to_string());
    lines.push(" * CpuState* and mutates it in place — no return values, no".to_string());
    lines.push(" * register-packed structs. Drop custom typedefs (HdmaPtrs,".to_string());
    lines.push(" * PairU8, OwHvPos, CollInfo, ExtCollOut, CheckPlatformCollRet,".to_string());
    lines.push(" * CalcTiltPlatformArgs) — all v1 ABI fiction, gone.".to_string());
    lines.push(" *".to_string());
    lines.push(" * Per-(m, x) variants: every named function is declared in".to_string());
    lines.push(" * five forms so cross-bank generated calls can reference any".to_string());
    lines.push(" * variant without a side-car header dependency. Unused".to_string());
    lines.push(" * variants are harmless (C only flags undefined REFERENCES,".to_string());
    lines.push(" * not undefined DECLARATIONS at link time).".to_string());
    lines.push(" */".to_string());
    lines.push("#pragma once".to_string());
    lines.push(String::new());
    lines.push("#include \"cpu_state.h\"".to_string());
    lines.push(String::new());
    lines.push(format!(
        "/* {} functions across all banks (5 decls each). */",
        items.len()
    ));
    lines.push(String::new());
    for (name, addr) in items {
        let bank = (addr >> 16) & 0xFF;
        let pc = addr & 0xFFFF;
        lines.push(format!(
            "void {name}(CpuState *cpu);  /* ${bank:02X}:{pc:04X} alias */"
        ));
        for em in 0..=1 {
            for ex in 0..=1 {
                lines.push(format!("RecompReturn {name}_M{em}X{ex}(CpuState *cpu);"));
            }
        }
    }
    lines.push(String::new());
    lines.push("/* Hand-written non-recompiled bodies still declared here.".to_string());
    lines.push(" * These are not produced by the v2 emit pipeline but are".to_string());
    lines.push(" * referenced by recompiled code via funcs.h.".to_string());
    lines.push(" */".to_string());
    lines.push("void ResetSpritesFunc(int wh);".to_string());
    lines.push("void RunOneFrameOfGame_Internal(void);".to_string());
    lines.push(String::new());
    lines.push("/* Watchdog hook called at every block label. Implemented in".to_string());
    lines.push(" * snesrecomp/runner/src/common_cpu_infra.c.".to_string());
    lines.push(" */".to_string());
    lines.push("void WatchdogCheck(void);".to_string());
    lines.push(String::new());
    lines.join("\n")
}

fn main() {
    let args: Vec<String> = std::env::args().collect();
    let cfg_dir = arg_value(&args, "--cfg-dir").expect("--cfg-dir required");
    let out = arg_value(&args, "--out").expect("--out required");

    let cfg_dir = std::path::Path::new(&cfg_dir);
    let out_path = std::path::Path::new(&out);

    let items = collect_function_names(cfg_dir);
    let src = emit_funcs_h(&items);

    if let Some(parent) = out_path.parent() {
        std::fs::create_dir_all(parent).ok();
    }
    std::fs::write(out_path, &src).expect("write funcs.h");
    println!("v2_sync_funcs_h: wrote {} declarations to {}", items.len(), out);
}
