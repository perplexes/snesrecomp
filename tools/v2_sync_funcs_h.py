"""snesrecomp.tools.v2_sync_funcs_h

Emit the v2-shape `recomp/funcs.h`: every function declared in any
bank cfg becomes a single `void <name>(CpuState *cpu);` forward
declaration. No more `RetAY` / `RetY` / `PairU16` / `HdmaPtrs` /
custom typedefs — those are pure v1 ABI fiction and die with the
rewrite.

Usage:
    python snesrecomp/tools/v2_sync_funcs_h.py \
        --cfg-dir SuperMarioWorldRecomp/recomp \
        --out SuperMarioWorldRecomp/recomp/funcs.h
"""

import argparse
import pathlib
import re
import sys

REPO = pathlib.Path(__file__).resolve().parent.parent
sys.path.insert(0, str(REPO / 'recompiler'))

from v2.cfg_loader import load_bank_cfg  # noqa: E402

_BANK_CFG_RE = re.compile(r'bank([0-9a-fA-F]+)\.cfg$')


def collect_function_names(cfg_dir: pathlib.Path) -> list:
    """Return a stable-ordered list of (addr, name) tuples covering every
    function declared in any bank cfg's `func` line, plus every `name`
    decl (cross-bank aliases). Deduplicated by name; first-seen address
    wins for diagnostics."""
    seen = {}  # name -> first-seen 24-bit address
    for cfg_path in sorted(cfg_dir.glob('bank*.cfg')):
        m = _BANK_CFG_RE.search(cfg_path.name)
        if not m:
            continue
        bank = int(m.group(1), 16)
        cfg = load_bank_cfg(str(cfg_path))
        for entry in cfg.entries:
            if entry.name and entry.name not in seen:
                seen[entry.name] = (bank << 16) | (entry.start & 0xFFFF)
        for nd in cfg.names:
            if nd.name and nd.name not in seen:
                seen[nd.name] = nd.addr_24
    return sorted(seen.items(), key=lambda kv: kv[1])


def emit_funcs_h(items: list) -> str:
    """Format the v2 funcs.h source string from a list of (name, addr)
    tuples ordered by addr.

    Declares each function in five forms:
      - bare name (the cfg-default alias emitted by emit_bank)
      - all four (m, x) variants: <name>_M0X0, _M0X1, _M1X0, _M1X1
    Only variants actually emitted by the active generated C will resolve at link time;
    unused-but-declared variants are harmless (C linker only flags
    undefined REFERENCES, not undefined declarations). This lets cross-
    bank calls reference whichever variant the caller's
    (m, x) selects without a side-car emit→header dependency.
    """
    lines = []
    lines.append('/* Auto-generated by snesrecomp v2 tools/v2_sync_funcs_h.py.')
    lines.append(' * Do NOT hand-edit. Every recompiled function takes a')
    lines.append(' * CpuState* and mutates it in place — no return values, no')
    lines.append(' * register-packed structs. Drop custom typedefs (HdmaPtrs,')
    lines.append(' * PairU8, OwHvPos, CollInfo, ExtCollOut, CheckPlatformCollRet,')
    lines.append(' * CalcTiltPlatformArgs) — all v1 ABI fiction, gone.')
    lines.append(' *')
    lines.append(' * Per-(m, x) variants: every named function is declared in')
    lines.append(' * five forms so cross-bank generated calls can reference any')
    lines.append(' * variant without a side-car header dependency. Unused')
    lines.append(' * variants are harmless (C only flags undefined REFERENCES,')
    lines.append(' * not undefined DECLARATIONS at link time).')
    lines.append(' */')
    lines.append('#pragma once')
    lines.append('')
    lines.append('#include "cpu_state.h"')
    lines.append('')
    lines.append(f'/* {len(items)} functions across all banks (5 decls each). */')
    lines.append('')
    # Bare-named alias keeps `void` ABI for hand-written callers
    # outside the v2 region. Each variant returns RecompReturn so
    # the SKIP_N non-local-return signal propagates cleanly through
    # the v2 call graph (see RecompReturn enum in cpu_state.h).
    for (name, addr) in items:
        bank = (addr >> 16) & 0xFF
        pc = addr & 0xFFFF
        lines.append(f'void {name}(CpuState *cpu);  /* ${bank:02X}:{pc:04X} alias */')
        for em in (0, 1):
            for ex in (0, 1):
                lines.append(f'RecompReturn {name}_M{em}X{ex}(CpuState *cpu);')
    lines.append('')
    lines.append('/* Hand-written non-recompiled bodies still declared here.')
    lines.append(' * These are not produced by the v2 emit pipeline but are')
    lines.append(' * referenced by recompiled code via funcs.h.')
    lines.append(' */')
    lines.append('void ResetSpritesFunc(int wh);')
    lines.append('void RunOneFrameOfGame_Internal(void);')
    lines.append('')
    lines.append('/* Watchdog hook called at every block label. Implemented in')
    lines.append(' * snesrecomp/runner/src/common_cpu_infra.c.')
    lines.append(' */')
    lines.append('void WatchdogCheck(void);')
    lines.append('')
    return '\n'.join(lines)


def main() -> int:
    p = argparse.ArgumentParser()
    p.add_argument('--cfg-dir', required=True)
    p.add_argument('--out', required=True)
    args = p.parse_args()

    cfg_dir = pathlib.Path(args.cfg_dir)
    out_path = pathlib.Path(args.out)

    addr_name_pairs = collect_function_names(cfg_dir)
    # Transpose to (name, addr) for emit.
    items = [(n, a) for (n, a) in addr_name_pairs]
    src = emit_funcs_h(items)

    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(src, encoding='utf-8', newline='\n')
    print(f"v2_sync_funcs_h: wrote {len(items)} declarations to {out_path}")
    return 0


if __name__ == '__main__':
    sys.exit(main())
